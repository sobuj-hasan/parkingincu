     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'uses_duration' => 'required',
            'uses_time' => 'required',
            'started_date' => 'required',
            'end_date' => '',
            'total_cost' => '',
        ]);

        $date1 = Carbon::createFromFormat('m/d/Y H:i:s', '12/08/2021 10:20:00');
        $date2 = Carbon::createFromFormat('m/d/Y H:i:s', '01/01/2022 10:20:00');

        $result = $date1 - ($date2);
        return $result;

        return $request->all();

    }

